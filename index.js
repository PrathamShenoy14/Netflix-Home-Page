
document.querySelectorAll(".dropdown")[0].addEventListener("click",function (){
    if(document.querySelectorAll(".dropdown-content")[0].classList.contains("show")){
        document.querySelectorAll(".dropdown-content")[0].classList.remove("show")
    }
    else{
        for(var i=0;i<6;i++){
            if(document.querySelectorAll(".dropdown-content")[i].classList.contains("show")){
                document.querySelectorAll(".dropdown-content")[i].classList.remove("show")
                break;
            }
            else{
                continue;
            }    
        }
        document.querySelectorAll(".dropdown-content")[0].classList.add("show")
    }
        
});

